createPhaserGame(_create);

function _create() {

    setBgColor("#000000");

    createSnowflakes2(0xff0000);

    autoTimedTexts(`
        Strufi e il bosco stregato
        Capitolo 3**
        *
        Strufi l'orsetto, dopo aver visto la neve rossa, prese il suo cappello da pescatore e uscì di casa.
        Non faceva freddo, anzi, la neve era tiepida. Strufi si fece cadere un fiocco di neve sulla lingua.
        Era dolciastro e sapeva di marmellata di ciliegie.
        "Fa fchifo vero? È un fapore peffimo!" disse Guglielmino.
        "Io lo trovo delizioso! Ma dove sono tutti gli animali?" chiese Strufi.
        "Non lo fo, io ftavo tornando dalla collina dei pafferotti fuffurranti e non c'era più neffuno!"
        Strufi cominciò a camminare notando che c'erano delle strane tracce sul terreno, come se gli animali fossero stati trascinati via.
        D'un tratto un fruscio, e in alto tra le foglie e i rami, svolazzava veloce su un aspirapolvere una donna dai lunghi capelli corvini e un mantello rosso.
        "Ah! - disse la donna sull'aspirapolvere - ci sono due creature qui! Possibile che non possa mai fidarmi di quei cialtroni dei miei Leccapatate?"
        E senza perdere tempo, la donna diede un colpo all'aspirapolvere per dirigersi verso l'orsetto e il suo amico castoro.
    `);

}