createPhaserGame(_create);

function _create() {

    setBgColor("#000000");

    createSnowflakes2(0xff0000);

    autoTimedTexts(`
        Strufi e il bosco stregato
        Capitolo 4**
        *
        La donna scese dall'aspirapolvere, rivelando un volto grinzoso e con un prominente neo sul naso. Il tipico aspetto delle streghe.
        Mosse i suoi lunghi capelli corvini e imbracciò l'aspirapolvere.
        "Creature pestifere, non potete nulla contro il mio Gyson V900!" disse la strega.
        Un rombo assordante colse di sorpresa Strufi e Guglielmino, che si misero a correre disperatamente.
        Guglielmino, nella foga, inciampò su una radice e cadde a terra. "Ftrufi! Ftrufi!" gridò il castoro.
        Strufi si fermò e fece per tornare a prendere l'amico, ma con un colpo di Gyson, il corpicino del piccolo castoro venne risucchiato.
        La strega fece per catturare anche Strufi ma i led dell'aspirapolvere indicarono che il sacchetto era pieno.
        "Sei fortunato, insipido orsetto peloso, ma manderò i miei leccapatate a prenderti! Hi Hi Hi!"
        La strega volò via e Strufi rimase solo, scioccato e disorientato.
    `);

}